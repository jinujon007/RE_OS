"""Shared Prometheus metrics for RE_OS runtime hooks.

All metric creation is idempotent — if a metric name already exists in the
global Prometheus registry, the existing metric is returned. This prevents
duplicate-registration errors during test collection when the same module
is imported via multiple paths.
"""

from prometheus_client import Counter, Histogram, REGISTRY as _R


def _counter(name: str, doc: str, labels: tuple = ()) -> Counter:
    existing = _R._names_to_collectors.get(name)
    if existing is not None:
        return existing
    try:
        return Counter(name, doc, labels)
    except ValueError:
        return _R._names_to_collectors.get(name)


def _histogram(name: str, doc: str, labels: tuple = (), buckets=None) -> Histogram:
    existing = _R._names_to_collectors.get(name)
    if existing is not None:
        return existing
    try:
        return Histogram(name, doc, labels, buckets=buckets)
    except ValueError:
        return _R._names_to_collectors.get(name)


