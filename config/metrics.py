_VALID_MARKETS = frozenset({"Yelahanka", "Devanahalli", "Hebbal", "all"})


def safe_scraper_market(market: str) -> str:
    if not market or not isinstance(market, str):
        return "unknown"
    cleaned = market.strip().title()
    if cleaned in _VALID_MARKETS:
        return cleaned
    return "unknown"


